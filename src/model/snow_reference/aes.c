void sub_bytes(u8 *state) {

    u8 i, j;
    u8 row, col;

    for (i = 0; i < 4; i++) {
        for (j = 0; j < 4; j++) {
            row = (state[4 * i + j] & 0xf0) >> 4;
            col = state[4 * i + j] & 0x0f;
            state[4 * i + j] = SBox[16 * row + col];
        }
    }
}

u8 gmult(u8 a, u8 b) {

    u8 p = 0, i = 0, hbs = 0;

    for (i = 0; i < 8; i++) {
        if (b & 1) {
            p ^= a;
        }

        hbs = a & 0x80;
        a <<= 1;
        if (hbs) a ^= 0x1b; // 0000 0001 0001 1011
        b >>= 1;
    }

    return (u8)p;
}

void coef_mult(u8 *a, u8 *b, u8 *d) {

    d[0] = gmult(a[0], b[0]) ^ gmult(a[3], b[1]) ^ gmult(a[2], b[2]) ^ gmult(a[1], b[3]);
    d[1] = gmult(a[1], b[0]) ^ gmult(a[0], b[1]) ^ gmult(a[3], b[2]) ^ gmult(a[2], b[3]);
    d[2] = gmult(a[2], b[0]) ^ gmult(a[1], b[1]) ^ gmult(a[0], b[2]) ^ gmult(a[3], b[3]);
    d[3] = gmult(a[3], b[0]) ^ gmult(a[2], b[1]) ^ gmult(a[1], b[2]) ^ gmult(a[0], b[3]);
}

void mix_columns(u8 *state)
{

    u8 a[] = { 0x02, 0x01, 0x01, 0x03 }; // a(x) = {02} + {01}x + {01}x2 + {03}x3
    u8 i, j, col[4], res[4];

    for (j = 0; j < 4; j++) {
        for (i = 0; i < 4; i++) {
            col[i] = state[4*i + j];
        }

        coef_mult(a, col, res);

        for (i = 0; i < 4; i++) {
            state[4*i + j] = res[i];
        }
    }
}

void shift_rows(u8 *state) {

    u8 i, k, s, tmp;

    for (i = 1; i < 4; i++) {
        s = 0;
        while (s < i) {
            tmp = state[4*i + 0];

            for (k = 1; k < 4; k++) {
                state[4*i + k - 1] = state[4*i + k];
            }

            state[4*i + 4 - 1] = tmp;
            s++;
        }
    }
}

void AESRound(u8 *in, u8 *out) {

    u8 state[4 * 4];
    u8  i, j;

    for (i = 0; i < 4; i++) {
        for (j = 0; j < 4; j++) {
            state[4*i + j] = in[i + 4 * j];
        }
    }
    sub_bytes(state);
    shift_rows(state);
    mix_columns(state);
    for (i = 0; i < 4; i++) {
        for (j = 0; j < 4; j++) {
            out[i + 4 * j] = state[4*i + j];
        }
    }
}
